document.addEventListener('DOMContentLoaded', () => {
    const navType = performance.getEntriesByType("navigation")[0]?.type;
    const fromSameDomain = document.referrer &&
                           new URL(document.referrer).hostname === window.location.hostname;

    if (navType === "reload") {
        createDots(); // always play on reload
    } else if (navType === "back_forward" || fromSameDomain) {
        createDots(true); // skip animation
    } else {
        createDots(); // fresh load from outside
    }
});