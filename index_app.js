function delay(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

function addBounce(obj) {
    obj.addEventListener('mouseenter', () => {
        obj.classList.add('bounce');

        setTimeout(() => {
            const currentColor = getComputedStyle(obj).backgroundColor;
            obj.style.boxShadow = currentColor === 'rgb(0, 0, 0)' ? '0 0 5px white' : '0 0 5px black';
            obj.style.backgroundColor = currentColor === 'rgb(0, 0, 0)' ? 'white' : 'black';
        }, 120);

        obj.addEventListener('animationend', () => {
            obj.classList.remove('bounce');
        }, { once: true });
    })
}

